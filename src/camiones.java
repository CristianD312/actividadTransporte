


public class camiones {
    
    private String marca;
    private int capacidad;
    private double litroxkm;

    public camiones(String marca, int capacidad, double litroxkm) {
        this.marca = marca;
        this.capacidad = capacidad;
        this.litroxkm = litroxkm;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public int getCapacidad() {
        return capacidad;
    }

    public void setCapacidad(int capacidad) {
        this.capacidad = capacidad;
    }

    public double getLitroxkm() {
        return litroxkm;
    }

    public void setLitroxkm(double litroxkm) {
        this.litroxkm = litroxkm;
    }
    
    
   
    
    
}
