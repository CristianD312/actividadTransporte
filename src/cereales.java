
public class cereales {

    private String nombre;
    private double precioxtonelada;

    public cereales(String nombre, double precioxtonelada) {
        this.nombre = nombre;
        this.precioxtonelada = precioxtonelada;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getPrecioxtonelada() {
        return precioxtonelada;
    }

    public void setPrecioxtonelada(double precioxtonelada) {
        this.precioxtonelada = precioxtonelada;
    }
    
    
    
    
    
    
}
